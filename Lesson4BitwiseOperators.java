public class Lesson4BitwiseOperators {

    public static void main(String[] args) {

        // Bitwise AND --> &

        boolean p = false;
        boolean q = false;

        System.out.println(p & q);

        // Bitwise inclusive OR --> |

        p = false;
        q = false;

        System.out.println(p | q);

        // Bitwise exclusive OR --> ^

        p = true;
        q = false;

        System.out.println(p ^ q);

        // All the outputs will work according to the truth table

    }
}